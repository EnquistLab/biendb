-- Label all Equisetopsida sensu lato
UPDATE bien_taxonomy
SET higher_plant_group=
CASE 
WHEN subclass='Cycadidae' THEN 'gymnosperms (non-conifer)'
WHEN subclass='Equisetidae' THEN 'ferns and allies'
WHEN subclass='Ginkgoidae' THEN 'gymnosperms (non-conifer)'
WHEN subclass='Gnetidae' THEN 'gymnosperms (non-conifer)'
WHEN subclass='Lycopodiidae' THEN 'ferns and allies'
WHEN subclass='Magnoliidae' THEN 'flowering plants'
WHEN subclass='Marattiidae' THEN 'ferns and allies'
WHEN subclass='Ophioglossidae' THEN 'ferns and allies'
WHEN subclass='Pinidae' THEN 'gymnosperms (conifers)'
WHEN subclass='Polypodiidae' THEN 'ferns and allies'
WHEN subclass='Psilotidae' THEN 'ferns and allies'
ELSE NULL
END
;

-- Mope up Equisetopsida with missing subclass
UPDATE bien_taxonomy
SET higher_plant_group='Embryophyte, classification unknown'
WHERE "class"='Equisetopsida' AND subclass is null
AND higher_plant_group is null
;

